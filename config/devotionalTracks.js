/**
 * 특정 트랙의 특정 주제만 추출 (헬퍼).
 *   예: getVersesByTheme('essentials100', 'prayer') → 기도 5구절.
 */
export function getVersesByTheme(trackId, themeId) {
    const t = getTrack(trackId);
    if (!t || !Array.isArray(t.verses)) return [];
    return t.verses.filter(v => v.theme === themeId);
}
